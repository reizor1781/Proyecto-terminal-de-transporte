    public boolean validarCampos() {
        if (txtCedula.getText().isEmpty() || txtContraseña.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        if (validarCampos()) {
            try {
                int id = Integer.parseInt(txtCedula.getText());
                String contraseña = txtContraseña.getText();
                Persona persona = controladorI.buscarInicioSesion(id, contraseña);
                if (persona instanceof AdminTerminal && persona != null) {
                    vistaAdminTerminal vt = new vistaAdminTerminal(persona);
                    vt.setVisible(true);
                    this.dispose();
                } else {
                    if (persona instanceof AdminFlota && persona != null) {
                        Caseta caseta = controladorI.buscarEmpresaDeAdminFlota((AdminFlota) persona);
                        if (caseta != null) {
                            VistaAdminFlota vf = new VistaAdminFlota(caseta,(AdminFlota)persona);
                            vf.setVisible(true);
                            this.dispose();
                        }
                    } else {
                        if (persona instanceof Cliente && persona != null) {
                            PruebaVistaCliente vc = new PruebaVistaCliente();
                            vc.setVisible(true);
                            this.dispose();
                        }
                    }
                }
            } catch (UsuarioNoEncontradoONoRegistradoException | AdminNoAsociadoAEmpresaException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "La cedula que ingreso no esta en el sistema o no tiene los caracteres correspondientes");
            }        
        } else {
            JOptionPane.showMessageDialog(null, "Por favor llene todos los espacios requeridos");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

